# 索引访问：一次只能访问一个字符
a = 'hello world'
a1 = a[2]    # a[2]代表‘hello world’从0开始的第3个字符 l
a2 = a[-10]  # a[-10]代表从最后一个字符，索引为-1开始往左索引，空格也表示一个字符

# 切片访问：一次能访问多个字符
b = 'hello python'
b1 = b[3:10]  # 访问第4个字符值第10个字符，区间为[3,10)
b2 = b[-9:-2]  # 同样访问第4个字符值第10个字符，区间为[-9,-2)
# 切片访问语法总结
c = 'hello python'
c1 = c[:4]    # 从0开始索引,直到索引3的字符被选出
c2 = c[:]     # 选择全部字符
c3 = c[::-1]  # 反向输出全部字符
c4 = c[::2]   # 步长为2选择字符
c5 = c[2:]    # 从索引2的字符开始直到结束
c6 = c[2::2]  # 从索引2的字符开始，从左往右以步长为2选择字符直到结束
